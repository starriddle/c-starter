/*
题目：
用*号输出字母C的图案。

分析：
可先用'*'号在纸上写出字母C，再分行输出。
*/

#include<stdio.h>

int main(int argc, char const *argv[])
{
    printf("   ***\n");
    printf(" *     *\n");
    printf("*\n");
    printf("*     Hello C-world!\n");
    printf("*\n");
    printf(" *     *\n");
    printf("   ***\n");
    return 0;
}
